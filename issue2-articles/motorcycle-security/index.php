                   <h3>Keep it out of sight</h3>
                   <p>Don’t leave your bike on show at home; try to put it away in a locked garage or sturdy shed. Anything that removes it from view is a bonus, even if you simply wheel it to your back garden or cover it with a tarpaulin; it’s better than nothing.</p>
