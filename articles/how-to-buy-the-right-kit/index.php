              <h2>Boots</h2>
              <p>A pair of sturdy over-the-ankle boots will provide support and protection for the ankle area and can prevent serious injuries, so it’s advised not to ride without them.</p>

              <p>In a crash, feet tend to get crushed sideways, so strong soles are important. A stitched sole is best and if possible bonded as well. Anything less and it may fall apart under impact or abrasion. The sole should contain ridges to provide good grip, and be at least 4mm thick. The thicker the sole, the more the boot will absorb the vibration of the bike.</p>

              <p>Boots come in varying levels of warmth and protection and range from a subtle black work style boot to multicoloured race replicas. Whatever style you go for, try them on to make sure they’re comfortable and allow you to operate the bike’s controls with ease.</p>
