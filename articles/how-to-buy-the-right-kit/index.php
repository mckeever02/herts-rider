              <h2>Gloves</h2>
              <p>Your hands are one of the first things to touch the ground if you come off your bike, so they must be protected. Fingers and wrists are fragile, so it doesn’t take much for them to sustain long-term damage. A pair of leather-palmed gloves will help take the sting out of a fall and prevent nasty injuries.</p>

              <p>Lighter gloves are available for summer riding with thermally insulated options for winter, but all-season options are available that can be worn all year round. Gloves should have a reinforced palm area to give increased abrasion resistance if you fall. Some also have hard plastic, metal or carbon fibre knuckle guards for added protection.</p>

              <p>Your hands are vulnerable in a crash and you can easily lose a finger if your hand gets trapped under you, so always wear specialist motorcycling gloves – a strong protective layer is essential.</p>

