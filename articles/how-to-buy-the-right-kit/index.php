              <h2>Trousers</h2>
              <p>Shorts and tracksuit bottoms are a real no-no when it comes to riding safely, as they offer zero protection in an accident. Jeans are slightly better, but will still wear through in under a second should the worst happen. A better option is to opt for a pair of jeans that are reinforced with Kevlar or other aramid fibre. Prices start at around £90 for a pair. The ultimate in protection however, is a pair of dedicated leather or textile trousers featuring double or triple stitching, plus CE-approved armour in the knees/shins.</p>

              <p>As with jackets, leather generally provides the most abrasion resistance, while textiles such as Cordura are waterproof and warm, so they can be used all year round.  Leather should be at least 1.2mm thick. Too thick and it’ll be uncomfortable and restrict your movement. Some will be treated to make it as waterproof as possible.</p>

