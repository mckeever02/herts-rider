              <p>Whether you ride a 50cc moped or a 1200cc superbike, riding in your everyday clothes puts you at real risk of serious injury. Finding clothing to shield you from both impact and abrasion – the most common causes of injury – is extremely important, as is keeping you warm and dry if the weather takes a turn for the worse.</p>

