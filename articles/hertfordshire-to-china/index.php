              <p>We all had a big ride ahead; 17 countries, 56 days and a massive mix of cultures, terrain, customs and officialdom is what I’m expecting as we set off from the Ace Café. I’d taste all of this and more, some to my delight and some to my outright despair.</p>

              <h2>Conquering Europe</h2>

              <p>Although Europe offers a diverse collection of cultures and experiences, the first days zipping through France, Germany, Austria and Slovenia had to be despatched so I could get on with the real adventure; some say that Europe is ‘in the way’ rather than ‘on the way’. The time spent riding the wonderful roads of France and the forest trails of Germany gave me time to acclimatise to living on the bike, packing and unpacking every night, and setting up a routine. It may not be the final frontier but it’s certainly a great training ground for anyone going further afield.</p>

              <p>Once I was out of the Euro Zone, border crossings become more of an event which need planning. Arrive too late and I’d be in a huge queue of trucks, too early will see me waiting for hours. I had the luxury of Kevin’s expertise and his experience of having ridden this route several times before. We generally got it right, had our paperwork in order and knew where to get money changed. Without this, it may have been a lottery in terms of timing and ease of passage. I soon learnt to be organised and business-like but always presented myself with a smile to get things done quickly.</p>

              <p>Cash is king in most of the countries I travelled through. None more so than Uzbekistan. The currency is the Som. The largest note is 2000 som which at the time was worth 17 pence. Exchanging a couple of hundred dollars resulted in a huge wad of notes that was hard to carry and store. An evening meal could easily be 500,000 som, or five bricks of notes which all needed counting; quite a challenge after a couple bottles of the local ‘sarbast’...</p>

              <h2>Roads scholar</h2>

              <div class="row">
                <div class="imgix-fluid-bg imgix-image" style="padding-bottom:50%;" data-src="<? echo $imgix_path ?><? echo $page_issue2[''.$page_id.'']['id'];?>/images/roads-scholar.jpg" alt="Roads scholar"></div>
              </div><!-- /row -->

              <p>Before departing I’d heard and read many accounts of what the roads would be like; I now know that road networks and tarmac surfaces come and go rapidly. A tarmac road through the Turkmenistan desert that’s travelled by a thousand trucks a day in the searing heat soon turns into a terrifying melted rut-covered trail that tests every element of your riding skills and courage. A rocky gravel trail through the mountains of Northern China can turn into a dual-carriageway just like that.</p>

              <p>Realistically, I could have ridden from London to China without doing any off-road riding, but I had to be prepared to encounter anything. Landslides aren’t always quickly sorted, sand can soon cover a desert road, and extreme weather in remote locations can change the landscape dramatically. Be prepared, carry water and food and be prepared to stop, and you’ll be fine.</p>

              <p>At the other extreme, China is rapidly laying huge lengths of motorway, which are smooth and straight. The miles would have quickly disappeared if I’d sat on one of these for a day, but I would have been denied the opportunity to take in rural China and I’d have missed the reason why I’d ridden this far in the first place. Besides, not all Chinese motorways allow motorcycles so we couldn’t count on being able to use them.</p>

              <h2>It’s official</h2>

              <div class="row">
                <div class="one-third column">
                    <div class="imgix-fluid-bg imgix-image" style="padding-bottom:150%;" data-src="<? echo $imgix_path ?><? echo $page_issue2[''.$page_id.'']['id'];?>/images/good-luck.jpg" alt="Good luck"></div>
