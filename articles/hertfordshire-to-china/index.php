                <div class="two-thirds column">
                  <p>The police and officials are generally interested in groups of foreign-plated motorcycles. Chinese officials want to chat and have a photo in exchange for a cold drink or watermelon. The Kazakhstan boys in blue were keen to chat and extract ‘Benjamins’ from us (dollars and the chance to ride away without any aggro). The best response is to say ‘no Benjamin’ and insist they write you a ticket which you can take to their boss for payment. The request soon goes away, even if you do get caught on camera doing four times the speed limit!</p>
                </div>
              </div>

              <p>Customs officers could be our worst nightmare or our best friend. A friendly one, as experienced entering Kazakhstan, would just want us out of the way and will do anything to avoid any additional paperwork. A bad one like the first guy I encountered in Turkmenistan arrested me for carrying prescription drugs and had me taken away into the desert for questioning and medical examination for 48 hours before telling me to leave their country. This part of the trip is a book in itself and an experience you should not try to replicate if you’re heading that way!</p>

