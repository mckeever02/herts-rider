              <p>The biggest unknown was how the Chinese would process us when crossing a border in the North West near Xining, as not many westerners on motorbikes come through that way. All the anticipation was put to rest when upon approaching, all the official staff where standing in a line waving and cheering. Welcome to China! After swift processing and a go in the X-raying machine we were sent on our way. This typified what we experienced throughout the three-week journey across their country.</p>

              <p>You must be prepared to wait. The longest wait I had was eight hours – in 45 degrees with no shade and only the water I was carrying. Once we started being processed we had to give our details to one person after another but the secret to success is to stay calm, and pay your entry fees along with your insurance. Don’t tell them you’re English and have rights unless you fancy the full treatment!</p>

              <h2>Me, myself and I</h2>

              <p>The joy of knowing there will be someone along soon to keep you company is brilliant; if you’re stranded on a desert road or freezing mountain side, the added security is comforting. The chance to share tales of the road every evening over a beer is wonderful and allowed us to fill in a few gaps or just kick back and sample the local delights in great company.</p>

              <p>On the road I preferred to ride by myself, but I didn’t really do enough of that. When you hook up with one or two others for the day your approach changes. If you’re navigating (I generally was) you have a feeling of greater responsibility, looking for fuel, food and hazards on behalf of everyone. Great for a few days but the freedom of being on your own and stopping when it suits you is liberating. One man and one bike looking at a map in the middle of the Kazakhstan steppe certainly confirms you’re on an adventure.</p>

