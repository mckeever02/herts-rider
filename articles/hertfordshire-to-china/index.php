              <p>This trip started out as a motorbike thing, but over time it became less and less about motorcycling. The things I saw, the surprise wow moments and the people I met were the real joys of a trip like this; the bike just facilitates things. Doing a similar trip by car would be very different. To many of the people in remote regions, you’re an alien on a huge alien motorcycle. They want to know who you are, where you’ve come from and where you’re going. Early in the trip I was keen to judge when the amazement of where I was going would be replaced by amazement at where I had come from. I reckon once in Turkmenistan, the other side of the Caspian Sea, people were more surprised by London than China.</p>


              <h2>New experiences</h2>

              <p>Every single mile of every single day offered me something I had probably never seen before and this is what adventure is about. There was no turning back; all situations had to be dealt with. Imagine waking up every morning and seeing new things and meeting new people for the first time, every day for two months. It’s a terrific experience that made me smile all day every day.</p>

              <p>I never had any preconception of what I would feel spending eight hours a day for two months inside my helmet. What would make me smile or what would get me down. Firstly, nothing got me down as every situation or challenge that presented itself was an adventure. Some of it was exhilarating, some fantastic and some just a bit of drag. But nothing was bad.</p>

              <p>Interestingly the least impact was made by all the sights that we have become accustomed to seeing. For example, the Great Wall of China is an amazing structure with a rich and incredible history, but I felt I’d seen it all before through the media. But riding around the corner on a dirt street to be faced with a city made of sand at Khiva in Uzbekistan was mind-blowing and made me literally go ‘wow’ in my helmet. The unexpected is more exciting than the scripted!</p>

              <p>The continuous change in people and cultures along the route is also a joy. It’s always an opportunity to learn something new, a different way of pouring tea or the new word for beer. You never get off the bike knowing exactly what’s going to happen, but thankfully we were generally welcomed with open arms by people who would give us everything although they had nothing. Ultimately everyone, everywhere, has the same basic needs, and the same basic routine – they just live somewhere different from you and I. </p>

              <p>The Chinese in rural areas were fascinated by our big bikes, the Georgians just want to hoot their horns and wave whilst the Uzbeks wanted to feed us. One thing they all have in common is a standard set of questions which are delivered by a variety of dodgy translations and mimes: Where are you going? Where do you come from? How much does the bike cost? How fast does it go?</p>

              <p>After a while you get a bit naughty with the answers and I now believe there are number of central Asian villagers who believe a BMW GSA costs $1million and has a top speed of 300mph...</p>

              <p>People are 99.9% good, at no point on this adventure did I feel threatened, concerned or unwelcome, (Turkmenistan excluded). Nothing was stolen but I did probably pay a few tourist rates for fuel. But then I was a tourist.</p>

              <h2>Food for thought</h2>

              <p>There’s one thing you can’t do without and that’s fuel for you and your bike. After hours of riding in the sun without hydrating, your decision-making abilities drop off. So drink little and frequently, get into the habit of regular wee stops (checking that it’s clear) to ensure you’ve been drinking enough. Stick with sealed bottles of water only or cans of soft drink. Urinating on the roadside is fine, bad guts and number twos is not!</p>

              <p>Food isn’t a problem. Everyone needs it and everyone eats it. The type of food available may be a little different from what you normally have but after over 50 kebabs of one style or another and a live frog you pretty much eat anything available if it’s fresh and cooked properly. Taking a look at the kitchen is a good idea; the locals aren’t embarrassed about it and you can eat in the knowledge that your meat on a stick didn’t used to be called Rover and chase cats.</p>

              <p>Regularly filling the GSA’s big tank took little planning, as most of the way we could source fuel. The BMW’s 400-mile range helped here. Fuel is generally exchanged only for cash, as cards are unloved outside Europe; the theory that US dollars are taken everywhere isn’t true though. Each country has its own currency and that’s what they want. Unless you’re a crooked Kazak policeman looking for a bonus!</p>

              <h2>At the end of the day...</h2>

              <p>As my trip was a Globebusters expedition, all accommodation was sorted out, pre-booked and paid for by them. ‘The best hotel in town’ was the plan. Sometimes this was a gleaming glass tower in a modern Chinese city, other times it was a modest industrial workers’ hotel in a provincial oil or mining town. There was only one occasion when the best hotel was the only hotel; in Kazakhstan near the Chinese border. A bit like staying at your gran’s 40 years ago, but the people were friendly.</p>

              <p>One night was spent under the stars at the amazing Darvaza gas crater in Turkmenistan, a tough and hot one that made the next night in Khiva even more welcome. Parking and security were never a problem and it’s hugely reassuring to have a GPS co-ordinate to head for each day for your hotel for the night; a safe target that sells beers..</p>

              <p>Being on the road for two months gives you time to think. A lot of what I thought about was the here and now and very little about any bigger picture. I thought about my family, lots. I thought about how simple life can be and deliver so much enjoyment. I thought about my bike and my kit and how well it all performed. And I thought a lot about the next trip..</p>

