              <p class="introduction">For enthusiasts Julie and Alex Bello, biking has been an escape, a stress relief mechanism, from the life of raising their 26-year old daughter Natasha, who suffers from multiple disabilities. As a result of being in a severe car crash as a baby, Natasha needs constant care and until recently these complexities gave the couple very little time to devote to the pursuit of happiness on two wheels.</p>
