              One day in 2012 the Bellos were at their local BMW motorcycle dealer <a href="http://www.sbwmotorrad-bmw.com">SBW Motorrad</a> when they saw a bike with a sidecar. The dealership’s Stephen Crowder is well known locally for his sidecar creations, as each one is bespoke. While he works to a regular basic design, the design of each rig is tweaked so it complements the bike to which it’s attached. SBW had already produced several sidecars but none was designed with a disabled occupant in mind.
            </p>


            <p>
              Says Julie: “It sparked an idea: what if we could increase our adventures on three wheels, taking our daughter with us? That spark grew to a smouldering pile of questions: how could we make it work? How would we get her in and out? How far could we travel? Will she get cold? The list was seemingly endless. These questions hung around for at least a year”.
            </p>

            <p>
