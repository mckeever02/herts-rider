            <p>Today’s XJR is different. It’s a fashion item. All the old traits are still there: simple, uncluttered lines, a big air-cooled 1250cc motor with more grunt than a 1970’s wrestling bout: but today’s XJR is all about less being more. When I say that, I mean less bodywork. The once long tail unit which could happily take you and the missus to the coast and back is now stunted, with a cut-down subframe supporting just an excuse for a curved pillion seat. The broad tank which once housed 21 litres of fuel is now narrow (and plastic) and holds just 14.5 litres, just so that the engine can jut out past the tank in the café racer fashion…</p>
