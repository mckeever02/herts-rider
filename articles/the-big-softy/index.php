            <p>Why just a while? Well, apart from the mirrors the only way the riding experience is affected by the style of this ‘new’ XJR is if you want to do any sort of distance. With a tiny tank you will see reserve by about 80 miles. I could barely squeeze a tenner’s worth of unleaded in the tank (about 10 litres) and the thing had been registering as being on reserve for a good 8-10 miles. Sometimes with the trip reading as low as 70 miles, you’d pop the thing on the sidestand and find it was telling you it was on reserve. Form has sadly won over function here, which is a shame as the XJR used to have much longer legs.</p>

