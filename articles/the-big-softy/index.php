            <p>You see, the modern XJR is aimed at a different breed of biker than the original back in the mid-1990s. There’s a trend on two wheels and it’s that of the biking hipster: someone who wants to ride a unique ‘one-off’ motorcycle special or café racer that makes them feel that little bit different. For Yamaha, they decided to make the XJR1300 this kind of bike. But did it work?</p>

            <p>Now, I’ve ridden XJRs since 1999 and loved them. XJRs were (for me) like big, friendly dogs: easy going, nice to look at and with plenty of bark when needed. I’d toured on an XJR and ridden an SP version around the track, so to swing a leg over this incarnation was going to be interesting. But I realised things have changed immediately.</p>

            <p>The old model’s big, rectangular chrome mirrors used to let you see whatever was behind you, but – for fashion’s sake – they’ve been replaced by tiny ones that show nothing but your elbows. That slimmed tank, smaller headlight and two strange oval number boards where the large side panels should be, also take away the butch, bruiser look of the original bike.</p>

            <p>Thankfully when you ride the new XJR, much is still the same. The scalloped seat is comfortable (for one person) and while she is a big bus, she can haul up her skirt and jig when she needs to. Helping here are the fantastic and fashionable Ohlins rear shocks, adjustable for rebound and preload which tell you what’s happening at the rear end. Up front are fully adjustable forks. I hardly touched the suspension, save for putting a bit more preload on the rear. Sure, the XJR isn’t a road scalpel like a sportsbike, but you soon get used to the minimal effort needed to nudge her into the next bend. Helping you get to and from each bend is the star performer in this package: the motor.</p>

