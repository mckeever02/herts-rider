            <h2>Your driving licence as a disabled driver</h2>

            <p>The problem with many medical conditions is they they can get worse, potentially affecting your ability to drive. So while the Driver and Vehicle Licensing Agency (DVLA) may be happy to grant you a licence initially, any deterioration of your condition may result in you having your licence taken away.</p>

            <p>The same goes if you develop a 'notifiable' medical condition or disability; the onus is on you to tell the DVLA about it. Such conditions would include epilepsy, strokes, other neurological and mental health conditions, or physical disabilities and visual impairments that could affect your ability to drive safely.</p>

            <p>If you fail to tell the DVLA about your circumstances, you could be fined up to £1000 – and if you’re involved in a crash because you’re not fit to drive, you could be prosecuted – so while losing your independence will be a wrench, you could lose a lot more if you keep driving regardless.</p>

