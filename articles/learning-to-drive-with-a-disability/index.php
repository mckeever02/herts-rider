            <p>If your disability is more severe you’ll probably have to drive a car that’s got some more comprehensive alterations, such as joystick or foot-operated steering. To pin down exactly what type of car you need – and whether or not you’re able to drive – you’ll need to contact one of the 17 <a href="http://www.mobility-centres.org.uk" target="_blank">Mobility Centres</a> in the UK.</p>

            <p>These centres can guide you through the various eligibility criteria for driving and steer you towards a local driving instructor who can help. They’ll also be able to tell you more about the various cars and modifications that are available, to help you get on the road.</p>

