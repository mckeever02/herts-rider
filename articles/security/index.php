                     <span class="circle-container">2</span>
                     <h3>Chain it up</h3>
                     <p>If leaving your bike on view is your only option, chain it to an immovable object such as a large tree or lamp post. Bikes aren’t that heavy and a thief can lift your bike into a van in seconds if it’s not secured to something. A good solution is a wall or ground anchor fitted to your house or driveway (or inside your garage), to which you can chain your bike. Some act as a bike stand that you wheel the bike into before locking it down with a chain, while others consist of a hardened metal loop that you pass your chain through. All need to be installed properly to be effective.</p>
